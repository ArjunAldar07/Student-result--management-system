    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed

        String rollmno=jTextField8.getText();
        String c=jTextField1.getText();
        String cpp=jTextField2.getText();
        String python=jTextField3.getText();
        String electronic=jTextField4.getText();
        String fundamental=jTextField9.getText();
        String english=jTextField6.getText();
        String maths=jTextField7.getText();
        
        try{
            
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/studentresult","root","root");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student where rollmno='"+rollmno+"'");
            if(rs.next())

            {
                st.executeUpdate("insert into bcs1(rollmno,c,cpp,python,electronic,fundamental,english,maths) values('"+rollmno+"','"+c+"','"+cpp+"','"+python+"','"+electronic+"','"+fundamental+"','"+english+"','"+maths+"')");
                JOptionPane.showMessageDialog(null,"record succesfully inserted");
                setVisible(false);
                new Insertnewresult().setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null,"rollno is not exsist!");
                setVisible(false);
                new Insertnewresult().setVisible(true);
            }

        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"connection error!");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

