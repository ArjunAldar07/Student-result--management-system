        try{
              Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/studentresult","root","root");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student inner join bcs1 where student.rollmno='"+rollmno+"' ");
            if(rs.next())
            {
                setVisible(false);
                new StudentHome1(rollmno).setVisible(true);
             }
            else
                JOptionPane.showMessageDialog(null, "incorrect roll number");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Connection error");
            
        }
