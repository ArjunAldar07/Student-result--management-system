    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        
        String course=(String)jComboBox1.getSelectedItem();
        String branch=(String)jComboBox2.getSelectedItem();
        String rollmno=jTextField1.getText();
        String name=jTextField2.getText();
        String gender=(String)jComboBox3.getSelectedItem();
        String fathername=jTextField3.getText();
        try{
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost/studentresult","root","root");
            Statement st=con.createStatement();
            st.executeUpdate("insert into student(rollmno,course,branch,name,gender,fathername) values ('"+rollmno+"','"+course+"','"+branch+"','"+name+"','"+gender+"','"+fathername+"')"); 
             JOptionPane.showMessageDialog(null, "successfully updated");
             setVisible(false);
             new adminHome().setVisible(true);
             st.close();
        }
        catch(Exception e)
        {
           JOptionPane.showMessageDialog(null, "this rollno is alredy exist");
           setVisible(false);
           new adminHome().setVisible(true);
            
        }
    }//GEN-LAST:event_jButton6ActionPerformed

